layer_state_t layer_state_set_user(layer_state_t state) {
    state = update_tri_layer_state(state, _SYMB, _NAV, _ADJUST);
    switch (get_highest_layer(state)) {
        case _QWERTY:
            set_led_off;
            break;
        case _SYMB:
            set_led_green;
            break;
        case _NAV:
            set_led_blue;
            break;
        case _ADJUST:
            set_led_red;
            break;
        default:
            break;
    }
  return state;
}


